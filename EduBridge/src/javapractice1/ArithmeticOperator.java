package javapractice1;

public class ArithmeticOperator 
{

	public static void main(String[] args)
	{
		int firstNumber=95;
		int secondNumber=7;
		System.out.println(firstNumber + secondNumber);
		System.out.println(firstNumber - secondNumber);
		System.out.println(firstNumber * secondNumber);
		System.out.println(firstNumber / secondNumber);
		System.out.println(firstNumber % secondNumber);
	
	}

}
